    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        conectar con=new conectar();
        Connection reg= con.conexion();
        String des,sueldo;
        String sql;
        
        des=descripcionCargo.getText();
        sueldo=sueldoCargo.getText();
        
        sql="INSERT INTO cargo (Descripcion, Sueldo) VALUES (?,?)";
        try {
            PreparedStatement pst= reg.prepareStatement(sql);
            pst.setString(1, des);
            pst.setString(2, sueldo);
            int n =pst.executeUpdate();
            if (n>0) {
                JOptionPane.showMessageDialog(null, "Cargo Registrado Correctamente");
            }
        } catch (SQLException ex) {
            Logger.getLogger(IngresoCliente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Error "+ex);
        }
        cargar();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
    String cargo="";
    int fila = tablaCargo.getSelectedRow();
    try {
        if(fila==-1)
        {
            JOptionPane.showMessageDialog(null, "No ha seleccionado ningun dato");
                  
        }
        else
        {
         cargo =  (String)tablaCargo.getValueAt(fila, 0);
         IngresoEmpleado.cargoEmpleado.setDisabledTextColor(Color.blue);
         IngresoEmpleado.cargoEmpleado.setText(cargo);
         this.dispose();
         
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "eRROR"+ e);
    }
    }//GEN-LAST:event_jButton6ActionPerformed

