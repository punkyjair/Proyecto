    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txtUsuario.getText().equalsIgnoreCase("admin") && txtClave.getText().equalsIgnoreCase("admin")){
            JOptionPane.showMessageDialog(this,"Bienvenido Administrador");
            UsuarioAdministrador a1 = new UsuarioAdministrador();
            a1.setVisible(true);
            dispose();
            
        } else  if(txtUsuario.getText().equalsIgnoreCase("mesero") && txtClave.getText().equalsIgnoreCase("mesero")) {
            JOptionPane.showMessageDialog(this,"Bienvenido Mesero");
            UsuarioMesero m1 = new UsuarioMesero();
            m1.setVisible(true);
            dispose();
        } else   if(txtUsuario.getText().equalsIgnoreCase("jefe") && txtClave.getText().equalsIgnoreCase("jefe")){
            JOptionPane.showMessageDialog(this,"Bienvenido Jefe de Cocina");
            UsuarioJefeCocina jefe1= new UsuarioJefeCocina();
            jefe1.setVisible(true);
            dispose();
        }else
        {
            JOptionPane.showMessageDialog(this,"Usuario/Clave INCORRECTO");

        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

