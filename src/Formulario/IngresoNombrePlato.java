    public void cargar(){
        String [] titulos={"CÃ³digo","Nombre","Tipo"};
        String [] registros= new String[3];
        String sql= "SELECT * FROM nombreplato";
        modelo = new DefaultTableModel(null, titulos);
        conectar cc= new conectar();
        Connection cn= cc.conexion();
        
        try {
            java.sql.Statement st= cn.createStatement();
            ResultSet rs= st.executeQuery(sql);
            while (rs.next()) {
                registros[0]= rs.getString("idNombrePlato");
                registros[1]= rs.getString("Nombre");
                registros[2]= rs.getString("TipoPlato_idTipoPlato");
                modelo.addRow(registros);
                tablaPlatos.setModel(modelo);
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(IngresoCargo.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, ex);
        }
    
    }
