    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        conectar con=new conectar();
        Connection reg= con.conexion();
        String des,tip;
        String sql;
        
        des=nomPlato.getText();
        tip=tipoPlato.getText();
        
        sql="INSERT INTO nombreplato (Nombre,TipoPlato_idTipoPlato) VALUES (?,?)";
        try {
            PreparedStatement pst= reg.prepareStatement(sql);
            pst.setString(1, des);
            pst.setString(2, tip);
            int n =pst.executeUpdate();
            if (n>0) {
                JOptionPane.showMessageDialog(null, "Nombre Plato Registrado Correctamente");
            }
        } catch (SQLException ex) {
            Logger.getLogger(IngresoCliente.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Error "+ex);
            JOptionPane.showMessageDialog(null,"Aqui");
        }
        cargar();
    }//GEN-LAST:event_jButton5ActionPerformed

